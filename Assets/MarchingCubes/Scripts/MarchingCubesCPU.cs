	Color32 SampleColor(Color32[] colors, Vector3 pos)
	{
		int x = (int)(pos.x);
		int y = (int)(pos.y);
		int z = (int)(pos.z);

		if (x < 0
		|| x >= _gridSize
		|| y < 0
