	float SampleData(Color32[] colors, Vector3 pos)
	{
		int x = (int)(pos.x);
		int y = (int)(pos.y);
		int z = (int)(pos.z);

		if (x < 0
		|| x >= _gridSize
		|| y < 0
		|| y >= _gridSize
		|| z < 0
		|| z >= _gridSize) return 0.0f;

		Color32 targetColor = colors[(_gridSize * _gridSize * z) + (_gridSize * y) + x];

		if (targetColor.a > 0)
			return 1.0f;
		else
			return 0.0f;
	}
