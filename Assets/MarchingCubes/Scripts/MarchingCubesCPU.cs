	public void SetVoxels(Color32[] colorBuffer, ref List<Vector3> outTriangles, ref List<Color32> outColors, ref List<int> outIndices)
	{
		outTriangles.Clear();
		outColors.Clear();
		outIndices.Clear();

		for (var z = 0; z < _gridSize; ++z)
		{
			for (var y = 0; y < _gridSize; ++y)
			{
				for (var x = 0; x < _gridSize; ++x)
				{
					MarchingCubes(colorBuffer, new Vector3(x, y, z), ref outTriangles, ref outColors);
				}
			}
		}

		for(int i=0; i<outTriangles.Count; i++) { outIndices.Add(i); }
	}

	public void MarchingCubes(Color32[] colorBuffer, Vector3 threadId, ref List<Vector3> outTriangles, ref List<Color32> outColors)
